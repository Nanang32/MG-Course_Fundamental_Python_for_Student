# Contoh penggunaan operator dasar
a = 10
b = 3

# Operator aritmatika
print("Penjumlahan:", a + b)
print("Pembagian bulat:", a // b)
print("Pangkat:", a ** b)

# Operator perbandingan
print("Apakah a lebih besar dari b?", a > b)

# Operator logika
print("Kondisi logika:", (a > 5) and (b < 5))

# Operator penugasan
a += 2
print("Nilai baru a setelah a += 2:", a)
